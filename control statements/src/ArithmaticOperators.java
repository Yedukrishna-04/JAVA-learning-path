public class ArithmaticOperators {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        //addition'+'
        System.out.println(a + b);
        //subtraction'-'
        System.out.println(a - b);
        //multiplication'*'
        System.out.println(a * b);
        //division'/'
        System.out.println(b/ a);
        System.out.println(a / (float) b);
        //reminder %
        System.out.println(a% b);
    }
}
