public class RelationalOperators {
    public static void main(String[] args) {
        int a = 10, b = 5;
        // is equal to
        System.out.println(a == b);
        //not equal to operator
        System.out.println(a != b);
        //greater than operator
        System.out.println(a > b);
        //less than operator
        System.out.println(a < b);
        //greater than equal to    }
        System.out.println(a >= b);
        //less than equal to
        System.out.println(a <= b);

    }
}
